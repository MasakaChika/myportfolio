	<h2>コンセプト</h2>
	<h3><strong>親しみやすい・わかりやすい・探しやすい</strong></h3>
	<p>私のポートフォリオサイトは、ユーザーの視点に立ったデザインを重視しています。サイトを訪れた方が直感的に使いやすく、必要な情報をスムーズに探し出せることを目指しました。</p>
	<ul>
		<li><strong>統一されたフォーマット・レイアウト</strong><br>すべてのページで一貫性のあるデザインを採用しています。これにより、ユーザーが迷わずに目的のコンテンツに辿り着けるよう配慮しています。</li>
		<li><strong>グルーピング</strong><br>作品をカテゴリ別にグループ化することで、関連するプロジェクトを一目で確認できるようにしました。これにより、ユーザーは興味のある分野の作品を簡単に見つけることができます。</li>
	</ul>

	<h3>作品につけられたタグによる検索機能</h3>
	<p>私のポートフォリオサイトでは、作品に付けられたタグを利用した検索機能を提供しています。これにより、ユーザーは特定のテーマや技術に関連する作品を素早く見つけることができます。</p>
	<ul>
		<li><strong>WORKS（制作一覧）の検索欄</strong><br>制作一覧ページでは、検索欄を設けており、作品に付けられたタグをもとに検索が可能です。</li>
		<li><strong>タグ毎の一覧にある検索欄</strong><br>タグごとの一覧ページでも検索機能を設けており、同じタグが付いた作品を簡単に絞り込むことができます。</li>
		<li><strong>個別作品ページのタグからの検索</strong><br>各作品ページでは、その作品に関連するタグが表示されており、タグをクリックすることで同じタグを持つ他の作品にすぐにアクセスできます。</li>
	</ul>

	<h2>専用の投稿タイプ</h2>
	<p>私のポートフォリオサイトでは、コンテンツの種類に応じて以下の専用投稿タイプを用意しています。これにより、各カテゴリのコンテンツを整理しやすく、ユーザーが必要な情報をすぐに見つけられるようにしています。</p>
	<ul>
		<li><strong>Works: 作品紹介専用の投稿タイプ</strong><br>ここでは、私が手掛けた作品を紹介しています。プロジェクトの詳細や使用技術、制作プロセスなどを詳しく説明しています。作品ごとにタグを付けているため、興味のあるテーマや技術に関連する作品を簡単に検索できます。</li>
		<li><strong>Journal: ジャーナルやブログ記事専用の投稿タイプ</strong><br>私の考えや日々の活動、業界のトレンドについてのブログ記事を掲載しています。技術的なトピックやデザインのインスピレーション、制作過程の裏話など、多岐にわたる内容をお届けしています。</li>
		<li><strong>News: お知らせ専用の投稿タイプ</strong><br>最新の情報やイベント、リリースのお知らせなど、重要な情報を掲載しています。ユーザーが最新の情報を見逃さないように、重要なお知らせを一目で確認できるようになっています。</li>
	</ul>

	<h2>技術的な詳細</h2>
	<h3>使用技術</h3>
	<p>私のポートフォリオサイトの構築には、以下の技術を使用しています。</p>
	<ul>
		<li><strong>HTML/CSS</strong><br>基本的な構造とスタイリングを担当しています。レスポンシブデザインを実現するために、最新のCSS技術を取り入れています。</li>
		<li><strong>JavaScript（Swiper.js、GSAP）</strong><br>動的なコンテンツ表示やアニメーションに利用しています。jQueryは使用せず、軽量かつモダンなJavaScriptライブラリを活用しています。</li>
		<li><strong>WordPress</strong><br>コンテンツ管理システムとしてWordPressを使用しています。これにより、コンテンツの更新が容易に行えます。</li>
	</ul>

	<h3>コーディングのポイント</h3>
	<p>私のポートフォリオサイトのコーディングには、以下のポイントを重視しました。</p>
	<ul>
		<li><strong>clampによる可変サイズの指定</strong><br>テキストや要素のサイズを柔軟に調整するために、CSSの<code>clamp()</code>関数を使用しています。これにより、異なるデバイスサイズに対しても最適な表示が可能です。</li>
		<li><strong>グリッドレイアウト（display: grid;）</strong><br>コンテンツのレイアウトにはCSSグリッドを使用しています。これにより、複雑なレイアウトもシンプルかつ効率的に実装することができます。</li>
		<li><strong>ブレイクポイント設定（mobile・tablet・desktop）</strong><br>レスポンシブデザインを実現するために、異なるデバイスサイズに対応したブレイクポイントを設定しています。これにより、スマートフォンからデスクトップまで、すべてのデバイスで最適な表示が保証されます。</li>
	</ul>

